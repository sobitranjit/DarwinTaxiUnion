            <h1>Background</h1>
            <p>
            While NT residents are quite familiar with the accessibility of various taxi services and Darwin radio taxi. Currently, taxi owners and drivers are not connected on a single platform, where they can directly communicate and discuss their availability of day-night shifts. This brings us to come up with a solution to provide drivers and owners a hosting communication platform in Darwin to boost maximum taxi to run on the road and almost every driver to get their shift accordingly.
            </p>

            <p>
                The objective of the project is to create a common platform that allows the drivers and Commercial passenger vehicle license (CPV) holder to make registration on the portal and assign their details including name and availability. Simultaneously, taxi owner or taxi provider can make registration on the website and can list the taxi details; if any available. Our vision is to make some improvements in the existing taxi system which in future will contribute to the Territory transportation economy. 
            </p>

         
